def main ():
    Num1 = float(input("Digite o numero : "))
    Num2 = float(input("Digite o numero : "))
    #adição
    print ( ' {} + {} = ' .format(Num1, Num2))
    print (Num1 + Num2 )

    #Subtração
    print ( ' {} - {} = ' .format(Num1, Num2))
    print (Num1 - Num2 )

    #Multiplicação
    print ( ' {} * {} = ' .format(Num1, Num2))
    print (Num1 * Num2 )

    #Divisão
    print ( ' {} / {} = ' .format(Num1, Num2))
    print (Num1 / Num2 )


main()
